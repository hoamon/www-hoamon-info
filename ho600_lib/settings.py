from django.contrib.auth.models import AnonymousUser
from django.http import HttpResponseForbidden
from django.conf import settings

if not hasattr(settings, 'CAN_VIEW_BUG_PAGE_IPS'):
    CAN_VIEW_BUG_PAGE_IPS = ['127.0.0.1']
if not hasattr(settings, 'CAN_VIEW_BUG_PAGE_DUSER'):
    CAN_VIEW_BUG_PAGE_DUSER = ['']


def checkCanViewBugPage(view_function):
    def innerFunction(*args, **kw):
        R = args[0]
        username = R.user.username if type(R.user) != AnonymousUser else '$$AnonymousUser$$'
        if (R.META.get('REMOTE_ADDR', '') in settings.CAN_VIEW_BUG_PAGE_IPS or
            (username in settings.CAN_VIEW_BUG_PAGE_DUSER)):
            return view_function(*args, **kw)
        else:
            return HttpResponseForbidden('You(%s) have no right to view this page'%username, mimetype='text/plain')
    innerFunction.func_name = view_function.func_name
    return innerFunction
    

